// Right 7, down 1.
// Right 1, down 2.
fn main() {

    
    
    let to_vector = INPUT.split('\n').collect::<Vec<_>>();
    let length = to_vector.len();
    let row_length = to_vector[0].len();

    const ARRAY_OF_MOVES: [[usize;2];5] = [[1,1],[3,1],[5,1],[7,1],[1,2]];
    let mut vec: [u32;5] = [0;5];


    for (vec_index , [right, down]) in ARRAY_OF_MOVES.iter().enumerate() {
        
        let mut ans = 0;
        let mut i  = 0;
        let mut j = 0;

        while i < length {
            j += right;
            j %= row_length;
            i += down;
            if i < length && to_vector[i].chars().nth(j).unwrap() == '#' {
                ans += 1;
            }
        }
        vec[vec_index] = ans;
    }
    
    println!("{}",vec.iter().product::<u32>())
    
}